package com.cardpay.pccredit.intopieces.web;

public class PicPojo {
	private String file_no;
	private String file_no_local;
	private String url;
	private String doc_id;
	private String file_name;
	private String pic_size;
	private String detail_id;
	
	public String getFile_no() {
		return file_no;
	}
	public void setFile_no(String file_no) {
		this.file_no = file_no;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDoc_id() {
		return doc_id;
	}
	public void setDoc_id(String doc_id) {
		this.doc_id = doc_id;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getPic_size() {
		return pic_size;
	}
	public void setPic_size(String pic_size) {
		this.pic_size = pic_size;
	}
	public String getDetail_id() {
		return detail_id;
	}
	public void setDetail_id(String detail_id) {
		this.detail_id = detail_id;
	}
	public String getFile_no_local() {
		return file_no_local;
	}
	public void setFile_no_local(String file_no_local) {
		this.file_no_local = file_no_local;
	}
}
